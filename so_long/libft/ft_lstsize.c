#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*temp;
	int		i;

	i = 0;
	temp = lst;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	lst = temp;
	return (i);
}
