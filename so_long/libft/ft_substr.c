#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sb;
	size_t	i;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	i = ft_strlen(s + start);
	if (i < len)
		len = i;
	sb = malloc(len + 1);
	if (!sb)
		return (NULL);
	ft_strlcpy(sb, (char *)s + start, len + 1);
	return (sb);
}
